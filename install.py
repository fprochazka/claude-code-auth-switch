#!/usr/bin/env python3
"""
Claude Code Multi-Account Profile Manager

Reads ~/.claude-profiles/config.yaml and creates isolated profile directories
with shared configuration, then installs wrapper scripts to bin_dir.
"""

import argparse
import json
import os
import difflib
import shutil
import stat
import sys
from pathlib import Path

import yaml

MARKER = "# Generated by claude-code-auth-switch"

# Files that Claude Code writes via atomic temp+rename (breaks symlinks)
COPY_FILES = [
    "settings.json",
    "settings.local.json",
    "CLAUDE.md",
]

# Directories that are safe to symlink (files inside are real)
SYMLINK_DIRS = [
    "commands",
    "skills",
    "agents",
    "plugins",
    "projects",
]

DEFAULT_CONFIG_PATH = Path("~/.claude-profiles/config.yaml").expanduser()
DEFAULT_SOURCE_DIR = Path("~/.claude").expanduser()
DEFAULT_BIN_DIR = Path("~/.local/bin").expanduser()


def load_config(config_path: Path) -> dict:
    with open(config_path) as f:
        config = yaml.safe_load(f)
    if not config or "profiles" not in config:
        print(f"Error: No profiles defined in {config_path}", file=sys.stderr)
        sys.exit(1)
    return config


def expand_path(path_str: str) -> Path:
    return Path(os.path.expanduser(path_str))


def setup_profile_dir(
    profile_name: str,
    profile_dir: Path,
    source_dir: Path,
    force_sync: bool,
) -> None:
    profile_dir.mkdir(parents=True, exist_ok=True)

    # Copy mutable files
    for filename in COPY_FILES:
        src = source_dir / filename
        dst = profile_dir / filename
        if not src.exists():
            continue
        if dst.exists() and not force_sync:
            continue
        shutil.copy2(str(src), str(dst))
        print(f"  {'synced' if force_sync else 'copied'} {filename}")

    # Create directory symlinks
    for dirname in SYMLINK_DIRS:
        src = source_dir / dirname
        dst = profile_dir / dirname
        if not src.exists():
            continue
        if dst.is_symlink():
            # Update symlink if target changed
            if dst.resolve() == src.resolve():
                continue
            dst.unlink()
        elif dst.exists():
            # Real directory exists, skip (don't replace user data with symlink)
            print(f"  skipped {dirname}/ (real directory exists, not replacing with symlink)")
            continue
        dst.symlink_to(src)
        print(f"  symlinked {dirname}/ -> {src}")


def sync_mcp_servers(
    profile_name: str,
    profile_dir: Path,
    source_claude_json_path: Path,
) -> None:
    """Sync mcpServers from the global ~/.claude.json into a profile's .claude.json.

    Only the mcpServers key is synced — all other keys in the profile's
    .claude.json are preserved untouched.
    """
    if not source_claude_json_path.exists():
        return

    try:
        source_data = json.loads(source_claude_json_path.read_text())
    except (json.JSONDecodeError, OSError):
        return

    source_servers = source_data.get("mcpServers")
    if source_servers is None:
        return

    profile_claude_json = profile_dir / ".claude.json"
    if not profile_claude_json.exists():
        return

    try:
        profile_data = json.loads(profile_claude_json.read_text())
    except (json.JSONDecodeError, OSError):
        return

    existing_servers = profile_data.get("mcpServers")

    # Check if already in sync
    if existing_servers == source_servers:
        return

    # Warn about profile-only servers being overwritten
    if existing_servers and isinstance(existing_servers, dict):
        source_names = set(source_servers.keys()) if isinstance(source_servers, dict) else set()
        profile_only = set(existing_servers.keys()) - source_names
        if profile_only:
            print(f"  note: overwriting profile-only mcpServers: {', '.join(sorted(profile_only))}")

    profile_data["mcpServers"] = source_servers
    profile_claude_json.write_text(json.dumps(profile_data, indent=2) + "\n")

    server_count = len(source_servers) if isinstance(source_servers, dict) else 0
    print(f"  synced mcpServers ({server_count} server{'s' if server_count != 1 else ''})")


def generate_wrapper_script(
    profile_name: str,
    profile_dir: Path,
    description: str,
    env_vars: dict[str, str],
    add_dirs: list[str],
    model: str | None = None,
) -> str:
    lines = [
        "#!/usr/bin/env bash",
        MARKER,
        f"# Profile: {profile_name} — {description}",
        "set -euo pipefail",
        "",
    ]

    # When a default model is configured, check if the user already passed --model
    # on the command line — user's --model always takes precedence.
    if model:
        lines.extend([
            f'# Default model (overridden if --model is passed on command line)',
            'user_args=("$@")',
            'has_model=false',
            'for arg in "$@"; do',
            '  case "$arg" in',
            '    --model|--model=*) has_model=true; break ;;',
            '  esac',
            'done',
            f'if [[ "$has_model" == false ]]; then',
            f'  user_args+=(--model "{model}")',
            'fi',
            '',
        ])

    lines.extend([
        "# Unset inherited CLAUDE_*/ANTHROPIC_* env vars to avoid interference",
        "# when nesting (running from within another Claude Code session).",
        'while IFS= read -r varname; do',
        '  unset "$varname"',
        'done < <(compgen -v | grep -E "^(CLAUDE_|ANTHROPIC_)")',
        "",
        f'export CLAUDE_CONFIG_DIR="{profile_dir}"',
        f'export FP_CC_AUTH_SWITCH_PROFILE="{profile_name}"',
    ])

    for key, value in sorted(env_vars.items()):
        lines.append(f'export {key}="{value}"')

    lines.append("")

    cmd_parts = ["exec claude"]
    for d in add_dirs:
        expanded = expand_path(d)
        cmd_parts.append(f'--add-dir "{expanded}"')
    cmd_parts.append('"${user_args[@]}"' if model else '"$@"')

    lines.append(" \\\n  ".join(cmd_parts))
    return "\n".join(lines) + "\n"


def install_wrapper_script(script_path: Path, content: str) -> None:
    script_path.parent.mkdir(parents=True, exist_ok=True)

    existing = None
    if script_path.exists():
        existing = script_path.read_text()

    if existing == content:
        return

    script_path.write_text(content)
    script_path.chmod(script_path.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
    print(f"  installed {script_path}")


def cleanup_stale_scripts(bin_dir: Path, active_script_names: set[str]) -> None:
    if not bin_dir.exists():
        return
    for entry in bin_dir.iterdir():
        if not entry.is_file():
            continue
        if entry.name in active_script_names:
            continue
        try:
            content = entry.read_text()
        except (OSError, UnicodeDecodeError):
            continue
        if MARKER in content:
            entry.unlink()
            print(f"  removed stale script {entry}")


def collect_diverged_files(
    config: dict,
    profiles_base: Path,
    source_dir: Path,
) -> list[tuple[str, str, str]]:
    """Check all profile copy files for divergence from source.

    Returns list of (profile_name, filename, diff_text) for files where
    the profile copy contains content not present in the source.
    """
    diverged = []
    for profile_name in config["profiles"]:
        profile_dir = profiles_base / profile_name
        for filename in COPY_FILES:
            src = source_dir / filename
            dst = profile_dir / filename
            if not src.exists() or not dst.exists():
                continue
            src_lines = src.read_text().splitlines(keepends=True)
            dst_lines = dst.read_text().splitlines(keepends=True)
            diff = list(difflib.unified_diff(
                src_lines, dst_lines,
                fromfile=str(src),
                tofile=str(dst),
            ))
            if not diff:
                continue
            # Check if profile has any additions (content not in source)
            has_additions = any(
                line.startswith('+') and not line.startswith('+++')
                for line in diff
            )
            if has_additions:
                diverged.append((profile_name, filename, ''.join(diff)))
    return diverged


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Install Claude Code multi-account profile wrapper scripts",
    )
    parser.add_argument(
        "--config",
        type=Path,
        default=DEFAULT_CONFIG_PATH,
        help=f"Path to config file (default: {DEFAULT_CONFIG_PATH})",
    )
    parser.add_argument(
        "--sync",
        action="store_true",
        help="Force re-copy of mutable files (overwrite existing copies in profile dirs)",
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Force overwrite of diverged profile files (use with --sync)",
    )
    args = parser.parse_args()

    config_path = args.config.expanduser()
    if not config_path.exists():
        print(f"Error: Config file not found: {config_path}", file=sys.stderr)
        print(f"Create it from config.example.yaml:", file=sys.stderr)
        print(f"  mkdir -p ~/.claude-profiles", file=sys.stderr)
        print(f"  cp config.example.yaml ~/.claude-profiles/config.yaml", file=sys.stderr)
        sys.exit(1)

    config = load_config(config_path)
    source_dir = expand_path(config.get("source_dir", str(DEFAULT_SOURCE_DIR)))
    bin_dir = expand_path(config.get("bin_dir", str(DEFAULT_BIN_DIR)))
    profiles_base = config_path.parent

    if not source_dir.exists():
        print(f"Error: Source directory not found: {source_dir}", file=sys.stderr)
        sys.exit(1)

    # When --sync is used without --force, check for diverged files first
    if args.sync and not args.force:
        diverged = collect_diverged_files(config, profiles_base, source_dir)
        if diverged:
            print("\nDiverged profile files detected:\n")
            for profile_name, filename, diff_text in diverged:
                print(f"  Profile '{profile_name}' — {filename}:")
                for line in diff_text.splitlines():
                    print(f"    {line}")
                print()
            print("The profile file(s) above contain changes not present in the source.")
            print("You probably want to merge these changes back to the source file first.")
            print("\nTo overwrite anyway, use: python3 install.py --sync --force")
            sys.exit(0)

    source_claude_json_path = Path.home() / ".claude.json"
    active_script_names: set[str] = set()

    for profile_name, profile_config in config["profiles"].items():
        profile_config = profile_config or {}
        description = profile_config.get("description", "")
        env_vars = profile_config.get("env", {}) or {}
        add_dirs = profile_config.get("add_dirs", []) or []
        model = profile_config.get("model") or None

        profile_dir = profiles_base / profile_name
        script_name = f"claude-{profile_name}"
        script_path = bin_dir / script_name
        active_script_names.add(script_name)

        print(f"\nProfile: {profile_name}")

        # Setup profile directory with copies and symlinks
        setup_profile_dir(profile_name, profile_dir, source_dir, args.sync)

        # Sync mcpServers from global ~/.claude.json
        sync_mcp_servers(profile_name, profile_dir, source_claude_json_path)

        # Generate and install wrapper script
        content = generate_wrapper_script(
            profile_name, profile_dir, description, env_vars, add_dirs, model,
        )
        install_wrapper_script(script_path, content)

    # Clean up stale wrapper scripts
    print("\nCleanup:")
    cleanup_stale_scripts(bin_dir, active_script_names)

    print("\nDone.")


if __name__ == "__main__":
    main()
